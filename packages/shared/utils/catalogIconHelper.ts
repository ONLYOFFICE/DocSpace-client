export const getCatalogIconUrlByType = (
  pageType: PageUnionType,
  options?: OptionsType,
): string => {
  const size: SizeType =
    isMobile() || isTablet() ? MobileIconSize : DesktopIconSize;

  const defaultIconUrl = options?.isSettingsCatalog
    ? NullURL
    : defaultIcon[size];

  return icons[size]?.[pageType] ?? defaultIconUrl;
};
